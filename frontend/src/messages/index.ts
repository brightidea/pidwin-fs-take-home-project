import { toast } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";

export const success = (message: string) =>
    toast.success(message, {
        position: "bottom-left",
        autoClose: 3000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
    });

